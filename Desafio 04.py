a = input('Digite algo para fazer algumas análises e testar seu tipo:')
print('O que foi digitado é do tipo:',type(a))
print('É numerico?',a.isnumeric())
print('É alfabetico?',a.isalpha())
print('É alfa numerico?',a.isalnum())
print('É minuscula?',a.islower())
print('É maiuscula?',a.isupper())
print('Está capitalizada?', a.istitle())
print('Tem espaços?',a.isspace())



